// Coercion refers to the process of converting a value from one data type to another. 
// This can happen explicitly or implicitly

// Explicit Coercion: This occurs when a developer intentionally converts a 
// value from one type to another using methods like Number(), String(), or Boolean().

// Implicit Coercion: This occurs when JavaScript programming language automatically converts a 
// value to a different type, often during operations like addition, subtraction, multiplication, or comparison.

// Example of implicit coercion
// Boolean to number //
console.log(true + 3);     //true = 1//
console.log(false - 2);    //false = 0//

