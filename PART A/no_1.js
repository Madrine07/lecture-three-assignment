// 1. Number: Represents a numeric value, which can be an integer or a 
// float value. Examples: 1, 2.5, -10.

// 2. String: Represents a sequence of characters, such 
// as text or  simple words. Strings are put in quotes(single or double). Examples: "Hello", 'Hello', "123".

// 3. Boolean: Represents a logical value, which can be either true or false.

// 4. Null: Represents the absence of any object value.

// 5. Undefined: Represents an uninitialized or non-existent variable.

// 6. Symbol: Represents a unique and immutable value. Symbols are often used as property keys in objects.

