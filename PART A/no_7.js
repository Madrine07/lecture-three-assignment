// Mutable Data Types:

// These are Data types that can be modified or changed after creation.

// Example:

// Objects:

let obj = { name: 'John' };
obj.age = 30; // obj is now { name: 'John', age: 30 }

console.log(obj)


// WHILE 

// Immutable Data Types:

// These are Data types that cannot be modified or changed after creation.

// Examples:

// -Numbers:

let num = 5;
num = 10; // creates a new number, does not modify the original
console.log(num)


